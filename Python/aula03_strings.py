"""
python = Linguagem de programação
Tipo de Tipagem = Dinâmica / Forte 
STR - > String -> Texto
Strings são texto que estão dentro de aspas
"""

# Aspas Simples
print('Luiz Otávio')
print(1, 'Luiz " Otávio"  ')

# Aspas duplas
print("Luiz Otávio")
print(2, "Luiz ' Otávio'  ")

# Escape
print("Luiz \" Otávio\"  ")

# r 
print(r"Luiz \" Otávio\"  ")