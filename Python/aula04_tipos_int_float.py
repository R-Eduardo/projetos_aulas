# Tipos int e float
# int -> Número inteiro
# O tipo int representa qualquer número
# positivo ou negativo int sem sinal é considerado 
# postivo 
print(11) #int
print(-11) #int
print(0)

# float -> Número com ponto flutuane
# tipo float representa qualquer número
# posito ou negativo com ponto flutuante.
# float sem sinal é considerado positivo
print(1.1, 10.11)
print(0.0, -1.2)

# a função type mostra o tipo que o python inferiu valor.
print(type('otávio'))
print(type(0))
print(type(0.0), type(0.0))